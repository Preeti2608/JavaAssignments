package Buildinfunctions;

public class MathFunctions {
    public static void main(String[] args) {
        // Returns the maximum of two numbers
        System.out.println("\nMax of 10 and 20: " + Math.max(10, 20));

        // Returns the minimum of two numbers
        System.out.println("Min of 10 and 20: " + Math.min(10, 20));

        // Returns the square root of a number
        System.out.println("Square root of 25: " + Math.sqrt(25));

        // Returns the value of 2 raised to the power 3 (2^3)
        System.out.println("2 raised to power 3: " + Math.pow(2, 3));

        // Returns the absolute (positive) value of -7
        System.out.println("Absolute value of -7: " + Math.abs(-7));

        // Returns a random number between 0.0 and 1.0
        System.out.println("Random number: " + Math.random());
    }
}

