// "This is a simple Java program to demonstrate how a for loop works. 
// The goal is to print the numbers from 1 to 5."
public class ForLoopExample {
    public static void main(String[] args) {
        // for (initialization; condition; update)
        for (int i = 1; i <= 5; i++) {
            System.out.println("\nCount: " + i);
        }
    }
}
